package com.StartupBBSR.competo.Models;

public class ManageEventMainItemModel {
    private String title, description, venue, image, date, time;

    public ManageEventMainItemModel(String title, String description, String venue, String image, String date, String time) {
        this.title = title;
        this.description = description;
        this.venue = venue;
        this.image = image;
        this.date = date;
        this.time = time;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getVenue() {
        return venue;
    }

    public void setVenue(String venue) {
        this.venue = venue;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
}
